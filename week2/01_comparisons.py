# Python101 - Lesson 2: Comparisons (True/False)

print("=== Comparisons ===")

a = 10
b = 7

print("a =", a)
print("b =", b)

print()  # blank line

print("Is a equal to b?        ->", a == b)
print("Is a NOT equal to b?    ->", a != b)
print("Is a greater than b?    ->", a > b)
print("Is a less than b?       ->", a < b)
print("Is a >= b?              ->", a >= b)
print("Is a <= b?              ->", a <= b)

print()
print("Done.")

# Teaching notes:
# - These checks produce True or False
# - The symbol == means “is equal to?”
# - The symbol = means “store a value”
